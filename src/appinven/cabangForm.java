    private void dtable(String pencarian, String status)
    {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Barang");
        model.addColumn("Barang");
        model.addColumn("Anggaran");
        model.addColumn("Realisasi");

        
        try {
            int jum=0;
            String sql;
            String pencarianText = txtPencarian.getText();
            String statusText  = status;

            
            sql = "SELECT b.kodebarang, b.barang, r.realisasi, b.anggaran FROM barang as b INNER JOIN realisasi as r "
                    + "ON b.id = r.barangId ";
            
            // jika user mencari di textifle pencarian
            if(!pencarianText.equals(""))
             sql += " WHERE b.barang LIKE '%" + pencarian + "%' ";
            
            // jika user merubah pencarian status
            // Semua Status, Sesuai, Surplus, Defisit
            if((!statusText.equals("")) && (!statusText.equals("Semua Status")))
            {
                sql += " WHERE ";
                if(status.equals("Sesuai")){
                    sql += "  realisasi = anggaran ";
                }else if(status.equals("Surplus")){
                    sql += "  realisasi < anggaran ";                    
                }else if(status.equals("Defisit")){
                    sql += "  realisasi > anggaran ";
                }
                
            }
            
            
//            sql += " ORDER BY r.id DESC";
            System.out.println(sql);
           
            Connection conn =(Connection)appinven.koneksi.koneksiDB();

            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
           
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString("barang"),res.getString("anggaran"),res.getString("realisasi")});
                jum++;
            }
            tableRealisasi.setModel(model);
            res.next();
            txtJumlah.setText( jum + " Baris");
            
           
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }


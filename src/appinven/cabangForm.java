    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        /**
         * Hanya boleh menyimpan jika sudah memilih barang (pencarian)
         */
        if(this.idBarang > 0)
        {
            
            try {
                if(Integer.parseInt(txtHarga.getText()) < 0)
                    throw new Exception("Harga Invalid");
                System.out.println(this.idBarang + " " +  Integer.parseInt(txtHarga.getText()) );
                String sql = "INSERT INTO realisasi (barangId, realisasi) "
                        + "VALUES (" + this.idBarang + "," + Integer.parseInt(txtHarga.getText()) + ")";
                Connection conn =(Connection)appinven.koneksi.koneksiDB();

                java.sql.PreparedStatement query=conn.prepareStatement(sql);
                if (!query.execute()){
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    bersih();

                    dtable("", "");
                }else{
                    throw new Exception("Gagal Login");

                }
            // total baris di sini 

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Data tidak lengkap");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

